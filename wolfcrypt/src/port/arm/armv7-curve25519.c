void fe_copy(fe r, const fe a)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        /* Copy */
        "ldrd	r2, r3, [r1]\n\t"
        "ldrd	r12, r4, [r1, #8]\n\t"
        "strd	r2, r3, [r0]\n\t"
        "strd	r12, r4, [r0, #8]\n\t"
        "ldrd	r2, r3, [r1, #16]\n\t"
        "ldrd	r12, r4, [r1, #24]\n\t"
        "strd	r2, r3, [r0, #16]\n\t"
        "strd	r12, r4, [r0, #24]\n\t"
        "add	sp, sp, #0\n\t"
        : [r] "+r" (r), [a] "+r" (a)
        :
        : "memory", "r2", "r3", "r12", "r4"
    );
}

void fe_sub(fe r, const fe a, const fe b)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        /* Sub */
        "ldrd	r12, r4, [r1]\n\t"
        "ldrd	r5, r6, [r1, #8]\n\t"
        "ldrd	r7, r8, [r2]\n\t"
        "ldrd	r9, r10, [r2, #8]\n\t"
        "subs	r7, r12, r7\n\t"
        "sbcs	r8, r4, r8\n\t"
        "sbcs	r9, r5, r9\n\t"
        "sbcs	r10, r6, r10\n\t"
        "strd	r7, r8, [r0]\n\t"
        "strd	r9, r10, [r0, #8]\n\t"
        "ldrd	r12, r4, [r1, #16]\n\t"
        "ldrd	r5, r6, [r1, #24]\n\t"
        "ldrd	r7, r8, [r2, #16]\n\t"
        "ldrd	r9, r10, [r2, #24]\n\t"
        "sbcs	r7, r12, r7\n\t"
        "sbcs	r8, r4, r8\n\t"
        "sbcs	r9, r5, r9\n\t"
        "sbc	r10, r6, r10\n\t"
        "mov	r11, #-19\n\t"
        "asr	r3, r10, #31\n\t"
        /*   Mask the modulus */
        "and	r11, r3, r11\n\t"
        "and	lr, r3, #0x7fffffff\n\t"
        /*   Add modulus (if underflow) */
        "ldrd	r12, r4, [r0]\n\t"
        "ldrd	r5, r6, [r0, #8]\n\t"
        "adds	r12, r12, r11\n\t"
        "adcs	r4, r4, r3\n\t"
        "adcs	r5, r5, r3\n\t"
        "adcs	r6, r6, r3\n\t"
        "adcs	r7, r7, r3\n\t"
        "adcs	r8, r8, r3\n\t"
        "adcs	r9, r9, r3\n\t"
        "adc	r10, r10, lr\n\t"
        "strd	r12, r4, [r0]\n\t"
        "strd	r5, r6, [r0, #8]\n\t"
        "strd	r7, r8, [r0, #16]\n\t"
        "strd	r9, r10, [r0, #24]\n\t"
        "add	sp, sp, #0\n\t"
        : [r] "+r" (r), [a] "+r" (a), [b] "+r" (b)
        :
        : "memory", "r3", "r12", "r4", "r5", "r6", "r7", "r8", "r9", "r10", "r11", "lr"
    );
}

void fe_add(fe r, const fe a, const fe b)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        /* Add */
        "ldrd	r12, r4, [r1]\n\t"
        "ldrd	r5, r6, [r1, #8]\n\t"
        "ldrd	r7, r8, [r2]\n\t"
        "ldrd	r9, r10, [r2, #8]\n\t"
        "adds	r7, r12, r7\n\t"
        "adcs	r8, r4, r8\n\t"
        "adcs	r9, r5, r9\n\t"
        "adcs	r10, r6, r10\n\t"
        "strd	r7, r8, [r0]\n\t"
        "strd	r9, r10, [r0, #8]\n\t"
        "ldrd	r12, r4, [r1, #16]\n\t"
        "ldrd	r5, r6, [r1, #24]\n\t"
        "ldrd	r7, r8, [r2, #16]\n\t"
        "ldrd	r9, r10, [r2, #24]\n\t"
        "adcs	r7, r12, r7\n\t"
        "adcs	r8, r4, r8\n\t"
        "adcs	r9, r5, r9\n\t"
        "adc	r10, r6, r10\n\t"
        "mov	r11, #-19\n\t"
        "asr	r3, r10, #31\n\t"
        /*   Mask the modulus */
        "and	r11, r3, r11\n\t"
        "and	lr, r3, #0x7fffffff\n\t"
        /*   Sub modulus (if overflow) */
        "ldrd	r12, r4, [r0]\n\t"
        "ldrd	r5, r6, [r0, #8]\n\t"
        "subs	r12, r12, r11\n\t"
        "sbcs	r4, r4, r3\n\t"
        "sbcs	r5, r5, r3\n\t"
        "sbcs	r6, r6, r3\n\t"
        "sbcs	r7, r7, r3\n\t"
        "sbcs	r8, r8, r3\n\t"
        "sbcs	r9, r9, r3\n\t"
        "sbc	r10, r10, lr\n\t"
        "strd	r12, r4, [r0]\n\t"
        "strd	r5, r6, [r0, #8]\n\t"
        "strd	r7, r8, [r0, #16]\n\t"
        "strd	r9, r10, [r0, #24]\n\t"
        "add	sp, sp, #0\n\t"
        : [r] "+r" (r), [a] "+r" (a), [b] "+r" (b)
        :
        : "memory", "r3", "r12", "r4", "r5", "r6", "r7", "r8", "r9", "r10", "r11", "lr"
    );
}

void fe_neg(fe r, const fe a)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        "mov	r6, #-1\n\t"
        "mov	r5, #-19\n\t"
        "ldrd	r2, r3, [r1]\n\t"
        "ldrd	r12, r4, [r1, #8]\n\t"
        "subs	r2, r5, r2\n\t"
        "sbcs	r3, r6, r3\n\t"
        "sbcs	r12, r6, r12\n\t"
        "sbcs	r4, r6, r4\n\t"
        "strd	r2, r3, [r0]\n\t"
        "strd	r12, r4, [r0, #8]\n\t"
        "mov	r5, #0x7fffffff\n\t"
        "ldrd	r2, r3, [r1, #16]\n\t"
        "ldrd	r12, r4, [r1, #24]\n\t"
        "sbcs	r2, r6, r2\n\t"
        "sbcs	r3, r6, r3\n\t"
        "sbcs	r12, r6, r12\n\t"
        "sbc	r4, r5, r4\n\t"
        "strd	r2, r3, [r0, #16]\n\t"
        "strd	r12, r4, [r0, #24]\n\t"
        "add	sp, sp, #0\n\t"
        : [r] "+r" (r), [a] "+r" (a)
        :
        : "memory", "r2", "r3", "r12", "r4", "r5", "r6"
    );
}

int fe_isnonzero(const fe a)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        "ldrd	r2, r3, [r0]\n\t"
        "ldrd	r12, r4, [r0, #8]\n\t"
        "ldrd	r5, r6, [r0, #16]\n\t"
        "ldrd	r7, r8, [r0, #24]\n\t"
        "adds	r1, r2, #19\n\t"
        "adcs	r1, r3, #0\n\t"
        "adcs	r1, r12, #0\n\t"
        "adcs	r1, r4, #0\n\t"
        "adcs	r1, r5, #0\n\t"
        "adcs	r1, r6, #0\n\t"
        "adcs	r1, r7, #0\n\t"
        "adc	r1, r8, #0\n\t"
        "asr	r1, r1, #31\n\t"
        "and	r1, r1, #19\n\t"
        "adds	r2, r2, r1\n\t"
        "adcs	r3, r3, #0\n\t"
        "adcs	r12, r12, #0\n\t"
        "adcs	r4, r4, #0\n\t"
        "adcs	r5, r5, #0\n\t"
        "adcs	r6, r6, #0\n\t"
        "adcs	r7, r7, #0\n\t"
        "adc	r8, r8, #0\n\t"
        "and	r8, r8, #0x7fffffff\n\t"
        "orr	r2, r2, r3\n\t"
        "orr	r12, r12, r4\n\t"
        "orr	r5, r5, r6\n\t"
        "orr	r7, r7, r8\n\t"
        "orr	r12, r12, r5\n\t"
        "orr	r2, r2, r7\n\t"
        "orr	%[a], r2, r12\n\t"
        "add	sp, sp, #0\n\t"
        : [a] "+r" (a)
        :
        : "memory", "r1", "r2", "r3", "r12", "r4", "r5", "r6", "r7", "r8", "r9"
    );
    return (uint32_t)(size_t)a;
}

int fe_isnegative(const fe a)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        "ldrd	r2, r3, [r0]\n\t"
        "ldrd	r12, r4, [r0, #8]\n\t"
        "adds	r1, r2, #19\n\t"
        "adcs	r1, r3, #0\n\t"
        "adcs	r1, r12, #0\n\t"
        "adcs	r1, r4, #0\n\t"
        "ldrd	r2, r3, [r0, #16]\n\t"
        "ldrd	r12, r4, [r0, #24]\n\t"
        "adcs	r1, r2, #0\n\t"
        "adcs	r1, r3, #0\n\t"
        "adcs	r1, r12, #0\n\t"
        "ldr	r2, [r0]\n\t"
        "adc	r1, r4, #0\n\t"
        "and	%[a], r2, #1\n\t"
        "lsr	r1, r1, #31\n\t"
        "eor	%[a], %[a], r1\n\t"
        "add	sp, sp, #0\n\t"
        : [a] "+r" (a)
        :
        : "memory", "r1", "r2", "r3", "r12", "r4"
    );
    return (uint32_t)(size_t)a;
}

