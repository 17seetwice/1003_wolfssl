void fe_init()
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        "\n\t"
        "add	sp, sp, #0\n\t"
        : 
        :
        : "memory"
    );
}

void fe_frombytes(fe out, const unsigned char* in)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        "ldrd	r2, r3, [r1]\n\t"
        "ldrd	r12, r4, [r1, #8]\n\t"
        "ldrd	r5, r6, [r1, #16]\n\t"
        "ldrd	r7, r8, [r1, #24]\n\t"
        "and	r8, r8, #0x7fffffff\n\t"
        "strd	r2, r3, [r0]\n\t"
        "strd	r12, r4, [r0, #8]\n\t"
        "strd	r5, r6, [r0, #16]\n\t"
        "strd	r7, r8, [r0, #24]\n\t"
        "add	sp, sp, #0\n\t"
        : [out] "+r" (out), [in] "+r" (in)
        :
        : "memory", "r2", "r3", "r12", "r4", "r5", "r6", "r7", "r8"
    );
}

void fe_tobytes(unsigned char* out, const fe n)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        "ldrd	r2, r3, [r1]\n\t"
        "ldrd	r12, r4, [r1, #8]\n\t"
        "ldrd	r5, r6, [r1, #16]\n\t"
        "ldrd	r7, r8, [r1, #24]\n\t"
        "adds	r9, r2, #19\n\t"
        "adcs	r9, r3, #0\n\t"
        "adcs	r9, r12, #0\n\t"
        "adcs	r9, r4, #0\n\t"
        "adcs	r9, r5, #0\n\t"
        "adcs	r9, r6, #0\n\t"
        "adcs	r9, r7, #0\n\t"
        "adc	r9, r8, #0\n\t"
        "asr	r9, r9, #31\n\t"
        "and	r9, r9, #19\n\t"
        "adds	r2, r2, r9\n\t"
        "adcs	r3, r3, #0\n\t"
        "adcs	r12, r12, #0\n\t"
        "adcs	r4, r4, #0\n\t"
        "adcs	r5, r5, #0\n\t"
        "adcs	r6, r6, #0\n\t"
        "adcs	r7, r7, #0\n\t"
        "adc	r8, r8, #0\n\t"
        "and	r8, r8, #0x7fffffff\n\t"
        "strd	r2, r3, [r0]\n\t"
        "strd	r12, r4, [r0, #8]\n\t"
        "strd	r5, r6, [r0, #16]\n\t"
        "strd	r7, r8, [r0, #24]\n\t"
        "add	sp, sp, #0\n\t"
        : [out] "+r" (out), [n] "+r" (n)
        :
        : "memory", "r2", "r3", "r12", "r4", "r5", "r6", "r7", "r8", "r9"
    );
}

void fe_1(fe n)
{
    __asm__ __volatile__ (
        "sub	sp, sp, #0\n\t"
        /* Set one */
        "mov	r2, #1\n\t"
        "mov	r1, #0\n\t"
        "strd	r2, r1, [r0]\n\t"
        "strd	r1, r1, [r0, #8]\n\t"
        "strd	r1, r1, [r0, #16]\n\t"
        "strd	r1, r1, [r0, #24]\n\t"
        "add	sp, sp, #0\n\t"
        : [n] "+r" (n)
        :
        : "memory", "r1", "r2"
    );
}

